// –ö–ª—é—á –¥–ª—è localStorage
export const TOUR_STORAGE_KEY = 'botvik_tour_completed';

// –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–æ—à—ë–ª –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Ç—É—Ç–æ—Ä–∏–∞–ª
export function isTourCompleted(): boolean {
  if (typeof window === 'undefined') return true;
  return localStorage.getItem(TOUR_STORAGE_KEY) === 'true';
}

// –û—Ç–º–µ—Ç–∏—Ç—å —Ç—É—Ç–æ—Ä–∏–∞–ª –∫–∞–∫ –ø—Ä–æ–π–¥–µ–Ω–Ω—ã–π
export function markTourCompleted(): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem(TOUR_STORAGE_KEY, 'true');
}

// –°–±—Ä–æ—Å–∏—Ç—å —Ç—É—Ç–æ—Ä–∏–∞–ª (–¥–ª—è –ø–æ–≤—Ç–æ—Ä–Ω–æ–≥–æ –ø—Ä–æ—Ö–æ–∂–¥–µ–Ω–∏—è)
export function resetTour(): void {
  if (typeof window === 'undefined') return;
  localStorage.removeItem(TOUR_STORAGE_KEY);
}

